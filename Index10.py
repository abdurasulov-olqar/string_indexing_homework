def main(s):
    """
    A string of five numbers is given. Find the sum of its numbers.
    Args:
        s(str): parameter
    Returns:
        int: answer
    """
    summa = 0

    summa += int(s[0])
    summa += int(s[1])
    summa += int(s[2])
    summa += int(s[3])
    summa += int(s[4])
    return summa